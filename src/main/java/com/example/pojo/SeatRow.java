package com.example.pojo;

public class SeatRow {
    String id;
    String name;
    int startingSeatNo;
    int endingSeatNo;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStartingSeatNo() {
        return startingSeatNo;
    }

    public void setStartingSeatNo(int startingSeatNo) {
        this.startingSeatNo = startingSeatNo;
    }

    public int getEndingSeatNo() {
        return endingSeatNo;
    }

    public void setEndingSeatNo(int endingSeatNo) {
        this.endingSeatNo = endingSeatNo;
    }
}
